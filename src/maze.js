export function getMap(level) {
  const layout = LAYOUTS[(level - 1) % LAYOUTS.length];
  return [...layout.top, ...GHOST_HOUSE, ...layout.bot];
}

export const MAP = getMap(1);

export const COLS = 28;
export const ROWS = 31;
export const TILE = 8;

// Veränderliche Kopie für aktuellen Level
export let grid = MAP.map(row => [...row]);
let _pelletCount = grid.flat().filter(t => t === T.PELLET || t === T.POWER).length;

function _countPellets() {
  _pelletCount = 0;
  for (const row of grid) for (const t of row) if (t === T.PELLET || t === T.POWER) _pelletCount++;
}

export function resetGrid(level = 1) {
  const m = getMap(level);
  grid = m.map(row => [...row]);
  _countPellets();
}

export function pelletsRemaining() {
  return _pelletCount;
}

export function tileAt(col, row) {
  if (row < 0 || row >= ROWS || col < 0 || col >= COLS) return T.WALL;
  return grid[row][col];
}

export function eatTile(col, row) {
  const t = grid[row][col];
  if (t === T.PELLET || t === T.POWER) _pelletCount--;
  grid[row][col] = T.EMPTY;
  return t;
}

export function isWall(col, row) {
  return tileAt(col, row) === T.WALL;
}

export function isWalkable(col, row) {
  const t = tileAt(col, row);
  return t !== T.WALL && t !== T.DOOR;
}

// Geister dürfen durch die Ghost-House Tür
export function isWalkableForGhost(col, row) {
  return tileAt(col, row) !== T.WALL;
}
